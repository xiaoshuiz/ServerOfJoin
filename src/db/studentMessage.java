package db;

public class studentMessage {
	public int cnumber;
	public String sname;
	public String password;
	public String sno;
	public String school;
	public String telnumber;
	public void set_cnumber(int cnumber)
	{
		this.cnumber=cnumber;
	}
	public int get_cnumber() 
	{
		return cnumber;
	}
	
	public void set_sname(String sname)
	{
		this.sname=sname;
	}
	public String get_sname() 
	{
		return sname;
	}
	
	public void set_password(String password)
	{
		this.password=password;
	}
	public String get_password() 
	{
		return password;
	}
	
	public void set_sno(String sno)
	{
		this.sno=sno;
	}
	public String get_sno() 
	{
		return sno;
	}
	
	public void set_school(String school)
	{
		this.school=school;
	}
	public String get_school() 
	{
		return school;
	}
	
	public void set_telnumber(String telnumber)
	{
		this.telnumber=telnumber;
	}
	public String get_telnumber() 
	{
		return telnumber;
	}
	
	

}
